%
%DESIGN AND CONTEXT
%
%Inputs
%------
%arr_len    (float)     arrow length
%arr_wid    (float)     arrow width
%midpoint   (float)     midpoint of line
%theta      (float)     angle of arrow
%
%Output
%------
%tri        (mat)       3x2 matrix of (x,y) coordinates of triangle points
%
%Dependent functions (excluding callbacks)
%-----------------------------------------
%rotate2D()

        %generate a virtual triangle around (0, 0)
        tri(1, :) = [-arr_wid/2 -arr_len/2];
        tri(2, :) = [0          arr_len/2];
        tri(3, :) = [arr_wid/2  -arr_len/2];
        
        tri = rotate2D(tri, theta - 90);
        
        %move triangle to mid-point of line
        tri(:, 1) = tri(:, 1) + midpoint(1);
        tri(:, 2) = tri(:, 2) + midpoint(2);
end

