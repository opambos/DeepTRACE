%you may not use this file except in compliance with the License.
%You may obtain a copy of the License at
%
%   http://www.apache.org/licenses/LICENSE-2.0
%
%Unless required by applicable law or agreed to in writing, software
%distributed under the License is distributed on an "AS IS" BASIS,
%WITHOUT WARRANTIES OR CONDITIONS OF ANY KIND, either express or implied.
%See the License for the specific language governing permissions and
%limitations under the License.
%
%
%DESIGN AND CONTEXT
%This takes the coordinates of two points, rotates them to horizontal,
%generates a midpoint, which is offset, applies a polyfit of order 2
%(quadratic) to generate 100 points on a curved line, and then rotates
%these points such that the end points of the lines match the original two
%points. The function also rotates the mid point, and returns this and theta
%so that the function calling this can place an arrowhead at this point.
%
%Input
%-----
%coords             (mat)       2x2 matrix of (x,y) coordinates, which are
%                                   the centres of two state circles
%midpoint_offset    (float)     offset of midpoint relative to the distance
%                                   between circle centres, larger here is
%                                   higher curvature,
%                                       0:  no curvature
%                                       1:  a semi circle
%                                       >1: higher than circular curvature
%                                       0.1 - 0.2: optimum
%
%Output
%------
%line               (mat)       100x2 matrix containing (x,y) coordinates
%                                   for 100 points defining the curved line
%                                   between two points
%midpoint           (vec)       row vector containing the coordinates of the midpoint
%theta              (float)     angle between line connecting the original two points
%
%Dependent functions (excluding callbacks)
%-----------------------------------------
%rotate2D()
    
    %calculate angle of rotation to horizontal, and rotate the points
    theta = atan2d(coords(2,2) - coords(1,2), coords(2,1) - coords(1,1));
    coords = rotate2D(coords, -theta);
    
    %compute and translate midpoint
    midpoint = mean(coords(1:2, :), 1);
    distance = norm(coords(2,:) - coords(1,:));
    midpoint(2) = midpoint(2) + midpoint_offset * distance;
    
    %generate curved line as quadratic fit to the three points
    x_vals = linspace(coords(1,1), coords(2,1), 100);
    points_for_fit = [coords(1:2,:); midpoint];
    p = polyfit(points_for_fit(:,1), points_for_fit(:,2), 2);
    y_vals = polyval(p, x_vals);
    line_points = [x_vals', y_vals'];
    
    %rotate points back to match original location
    line_points = rotate2D(line_points, theta);
    midpoint    = rotate2D(midpoint, theta);    
end


function [tri] = positionTri(arr_len, arr_wid, midpoint, theta)
%Position triangle at midpoint of curved line, 14/11/2023.
%
%Author: Oliver J. Pambos, Department of Physics, University of Oxford, UK
%(oliver.pambos@physics.ox.ac.uk).
%
